/**
 * The Standard Template Library (STL) is a powerful set of C++ template classes to provide general-purpose classes and functions with templates that implement many popular and commonly used algorithms and data structures like vectors, lists, queues, and stacks.
 * 
 * STL has four components:
 * 1. Algorithms: A collection of functions for various operations like searching, sorting, and manipulating data.
 * 2. Containers: Data structures like vectors, lists, deques, sets, and maps that store collections of objects.
 * 3. Iterators: Objects that enable traversal of elements in a container.
 * 4. Functors: Function objects that can be used in place of functions.
 * 
 * STL provides a standardized way to work with data structures and algorithms, making code more reusable and easier to maintain.
 */
